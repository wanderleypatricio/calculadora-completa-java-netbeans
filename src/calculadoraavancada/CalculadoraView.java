    private void calcular(String op){
        /*verifica se o operador √© de soma caso seja precisa trocar por um 
        outro caracter pra representar a soma se n√£o ocorre erro na aplica√ß√£o*/
        if(op.equals("+")){
            op = "M";
        }
        
        /*
        aqui criamo um vetor para pegar os valores da opera√ß√£o a ser realizada.
        O comando replace(",", ".") remove a virgula caso o usu√°rio digite n√∫mero quebrado
        e substitui pelo ponto. O segundo comando replace("+", op) troca o sinal de + pelo M para indicar
        que quero realizar um soma caso o operador seja de soma j√° que o sinal de + n√£o consegue ser interpretado pelo linguagem.
        O comando split() √© usado para fazer quebra de strings usando uma referencia de quebra, nesse caso o que vai ser usado 
        como referencia para quebra vai ser o operador.
        
        O vetor num vai armazenar dois n√∫meros a cada opera√ß√£o.
        */
        String [] num = campoResultado.replace(",", ".").replace("+", op).split(op);
        
        /*
        Aque passamos os valores armazenados no vetor num para as variaveis num1 e num2 para podermos realizar a opera√ß√£o.
        O comando trim() √© usado para remover qualquer espa√ßo que exista.
        O comando Double.parseDouble() serve para fazer a convers√£o de String para double para poder realizar a opera√ß√£o.
        */
        double num1 = Double.parseDouble(num[0].trim());
        double num2 = Double.parseDouble(num[1].trim());
        
        //vari√°vel para armazenar o resultado da opera√ß√£o
        double result = 0;
        
        
        /*
           nessa estrutura verificamos qual a opera√ß√£o a ser realizada.
        */
        if(op.equals("x")){
            result = num1 * num2;
        }else if(op.equals("/")){
            result = num1 / num2;
        }else if(op.equals("-")){
            result = num1 - num2;
        }else if(op.equals("M")){
            result = num1 + num2;
        }
        
        //a cada opera√ß√£o precisamos limpar a vari√°vel para poder atribuir o resultado e poder realizar outra opera√ß√£o
        campoResultado = "";
        txtResultado.setText("");
        
        //Aqui fazemos com que o resultado seja exibido na tela
        setTxtResultado(String.valueOf(result).replace(".0", "").replace(".", ","));
        
        //a vari√°vel result e op precisam ser zeradas para n√£o cumular valores anteriores
        result = 0;
        op = "";
    }
